package com.example.galadarbs.models;

public class SongList {
    private String name;
    private int picture;

    public SongList(String name, int picture) {
        this.name = name;
        this.picture = picture;
    }

    public String getName() {
        return name;
    }

    public int getPicture() {
        return picture;
    }
}




